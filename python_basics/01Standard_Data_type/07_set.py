# Set Example
colors = {"red", "blue", "green"}
print(f"Original Set: {colors}")
colors.add("yellow")
print(f"After Add: {colors}")
colors.remove("blue")
print(f"After Remove: {colors}")
print(f"Is 'red' in Set? {'red' in colors}")

