# Integer Example
'''The f in your code is used to create an f-string (short for formatted string literal), a feature introduced in Python 3.6. It allows you to embed expressions directly within a string by placing them inside curly braces {}.'''
num = 10
print(f"Original number: {num}")
print(f"Addition: {num + 5}")
print(f"Multiplication: {num * 2}")
print(f"Exponentiation: {num ** 2}")
print(f"Is Even? {'Yes' if num % 2 == 0 else 'No'}")

