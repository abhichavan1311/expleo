# String Example
message = "Hello, Python!"
print(f"Original: {message}")
print(f"Uppercase: {message.upper()}")
print(f"Lowercase: {message.lower()}")
print(f"Length: {len(message)}")
print(f"Substring: {message[7:13]}")
print(f"Split by comma: {message.split(',')}")

