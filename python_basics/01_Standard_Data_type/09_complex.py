# Complex Number Example
complex_num = 3 + 5j
print(f"Original: {complex_num}")
print(f"Real Part: {complex_num.real}")
print(f"Imaginary Part: {complex_num.imag}")
print(f"Conjugate: {complex_num.conjugate()}")


