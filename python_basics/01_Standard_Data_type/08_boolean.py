# Boolean Example
is_active = True
print(f"Is Active: {is_active}")
print(f"Not Active: {not is_active}")
print(f"Boolean AND Example: {is_active and False}") #The and operator returns True if both operands are True; otherwise, it returns False.
print(f"Boolean OR Example: {is_active or False}")  #The or operator returns True if at least one of the operands is True.

